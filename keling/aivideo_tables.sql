-- AI旅拍功能数据库表结构
-- 创建时间: 2026-01-19
-- 功能描述: AI旅拍是一款面向旅游景区和摄影商家的智能化AI视频生成平台

-- AI旅拍商家配置表
CREATE TABLE IF NOT EXISTS `ddwx_aivideo_merchant_config` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT COMMENT '主键ID',
  `aid` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '应用ID',
  `bid` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '商家ID',
  `merchant_name` varchar(100) NOT NULL DEFAULT '' COMMENT '商家名称',
  `access_key` varchar(255) NOT NULL DEFAULT '' COMMENT '可灵AI AccessKey',
  `secret_key` varchar(255) NOT NULL DEFAULT '' COMMENT '可灵AI SecretKey',
  `api_url` varchar(255) NOT NULL DEFAULT 'https://api-beijing.klingai.com' COMMENT 'API接口地址',
  `model_name` varchar(50) NOT NULL DEFAULT 'kling-v1' COMMENT '默认模型名称',
  `mode` varchar(20) NOT NULL DEFAULT 'std' COMMENT '生成模式:std标准,pro专家',
  `aspect_ratio` varchar(20) NOT NULL DEFAULT '16:9' COMMENT '默认画幅比例:16:9,9:16,1:1',
  `duration` varchar(10) NOT NULL DEFAULT '5' COMMENT '默认视频时长(秒)',
  `monitor_path` varchar(500) NOT NULL DEFAULT '' COMMENT '本地监控文件夹路径',
  `auto_upload` tinyint(1) NOT NULL DEFAULT '1' COMMENT '是否自动上传:1是,0否',
  `status` tinyint(1) NOT NULL DEFAULT '1' COMMENT '状态:1启用,0禁用',
  `createtime` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '创建时间',
  `updatetime` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_aid` (`aid`),
  KEY `idx_bid` (`bid`),
  KEY `idx_status` (`status`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='AI旅拍商家配置表';

-- AI旅拍素材表
CREATE TABLE IF NOT EXISTS `ddwx_aivideo_material` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT COMMENT '主键ID',
  `aid` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '应用ID',
  `bid` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '商家ID',
  `mid` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '会员ID(游客)',
  `material_name` varchar(200) NOT NULL DEFAULT '' COMMENT '素材名称',
  `material_type` varchar(20) NOT NULL DEFAULT 'image' COMMENT '素材类型:image图片,video视频',
  `material_url` varchar(500) NOT NULL DEFAULT '' COMMENT '素材URL',
  `material_path` varchar(500) NOT NULL DEFAULT '' COMMENT '本地文件路径',
  `file_size` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '文件大小(字节)',
  `width` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '图片宽度',
  `height` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '图片高度',
  `upload_type` varchar(20) NOT NULL DEFAULT 'manual' COMMENT '上传方式:manual手动,auto自动监控',
  `upload_source` varchar(100) NOT NULL DEFAULT '' COMMENT '上传来源',
  `status` tinyint(1) NOT NULL DEFAULT '1' COMMENT '状态:1正常,0删除',
  `createtime` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '创建时间',
  PRIMARY KEY (`id`),
  KEY `idx_aid` (`aid`),
  KEY `idx_bid` (`bid`),
  KEY `idx_mid` (`mid`),
  KEY `idx_status` (`status`),
  KEY `idx_createtime` (`createtime`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='AI旅拍素材表';

-- AI旅拍任务表
CREATE TABLE IF NOT EXISTS `ddwx_aivideo_task` (
  `id` int(11) unsigned NOT NULL AUTO_INCREMENT COMMENT '主键ID',
  `aid` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '应用ID',
  `bid` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '商家ID',
  `mid` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '会员ID(游客)',
  `task_type` varchar(50) NOT NULL DEFAULT 'image2video' COMMENT '任务类型:image2video图生视频,text2video文生视频,effects视频特效',
  `task_name` varchar(200) NOT NULL DEFAULT '' COMMENT '任务名称',
  `material_id` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '素材ID',
  `material_url` varchar(500) NOT NULL DEFAULT '' COMMENT '素材URL',
  `prompt` text NOT NULL COMMENT '提示词',
  `negative_prompt` text COMMENT '负向提示词',
  `model_name` varchar(50) NOT NULL DEFAULT 'kling-v1' COMMENT '模型名称',
  `mode` varchar(20) NOT NULL DEFAULT 'std' COMMENT '生成模式:std标准,pro专家',
  `aspect_ratio` varchar(20) NOT NULL DEFAULT '16:9' COMMENT '画幅比例:16:9,9:16,1:1',
  `duration` varchar(10) NOT NULL DEFAULT '5' COMMENT '视频时长(秒)',
  `effect_scene` varchar(100) COMMENT '特效场景名称',
  `external_task_id` varchar(100) COMMENT '自定义任务ID',
  `kling_task_id` varchar(100) COMMENT '可灵AI任务ID',
  `task_status` varchar(20) NOT NULL DEFAULT 'pending' COMMENT '任务状态:pending待处理,submitted已提交,processing处理中,succeed成功,failed失败',
  `task_status_msg` varchar(500) COMMENT '任务状态信息',
  `callback_url` varchar(500) COMMENT '回调地址',
  `request_data` text COMMENT '请求数据(JSON)',
  `response_data` text COMMENT '响应数据(JSON)',
  `retry_count` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '重试次数',
  `createtime` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '创建时间',
  `updatetime` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '更新时间',
  `completetime` int(11) unsigned NOT NULL DEFAULT '0' COMMENT '完成时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `idx_external_task_id` (`external_task_id`),
  KEY `idx_aid` (`aid`),
  KEY `idx_bid` (`bid`),
  KEY `idx_mid` (`mid`),
  KEY `idx_kling_task_id` (`kling_task_id`),
  KEY `idx_task_status` (`task_status`),
  KEY `idx_createtime` (`createtime`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='AI旅拍任务表';

